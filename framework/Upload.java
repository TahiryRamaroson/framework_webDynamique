package etu1849.framework;

public class Upload {
    String name;
    byte[] file;

    public Upload() {}

    public Upload(String name, byte[] file) {
        this.name = name;
        this.file = file;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public byte[] getFile() {
        return file;
    }

    public void setFile(byte[] file) {
        this.file = file;
    }
}
